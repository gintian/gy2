package com.hjsj.hrms.businessobject.sys.options;

public class PrivBean {

	private String functionPriv;
	private String dbPriv;
	private String managePriv;
	private String formula;
	private String tablePriv;
	private String fieldPriv;
	private String resourcePriv;
	
	
	public String getDbPriv() {
		return dbPriv;
	}
	public void setDbPriv(String dbPriv) {
		this.dbPriv = dbPriv;
	}
	public String getFieldPriv() {
		return fieldPriv;
	}
	public void setFieldPriv(String fieldPriv) {
		this.fieldPriv = fieldPriv;
	}
	public String getFormula() {
		return formula;
	}
	public void setFormula(String formula) {
		this.formula = formula;
	}
	public String getFunctionPriv() {
		return functionPriv;
	}
	public void setFunctionPriv(String functionPriv) {
		this.functionPriv = functionPriv;
	}
	public String getManagePriv() {
		return managePriv;
	}
	public void setManagePriv(String managePriv) {
		this.managePriv = managePriv;
	}
	public String getResourcePriv() {
		return resourcePriv;
	}
	public void setResourcePriv(String resourcePriv) {
		this.resourcePriv = resourcePriv;
	}
	public String getTablePriv() {
		return tablePriv;
	}
	public void setTablePriv(String tablePriv) {
		this.tablePriv = tablePriv;
	}
	
	
	
}
